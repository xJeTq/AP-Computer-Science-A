
public class Player1 {

	private int health = 100;
	private int damage = 10;
	
	public Player1(int h, int d) {
		
		this.health = h;
		this.damage = d;
		
	}
	
	public int getHealth() {
		
		return health;
		
	}
	
	public void setHealth(int h) {
		
		this.health = h;
		
	}
	
	public int getDamage() {
		
		return damage;
		
	}
	
	public void setDamage(int d) {
		
		this.damage = d;
		
	}
	
	
}
